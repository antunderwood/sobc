                    She is reminiscing over her life, and most of it is filled with sadness. She struggles with why it is so hard to say, "I love you" to Jim. In the end she decides that it is because there wasn't a lot of love in her life. Her mum didn't want her around, and neither did the men that her mum had home. A few people in her life briefly gave her moments of happiness. Being evacuated as a child and staying with a kind aunt and uncle was the most powerful and hardest to lose. Her adult experience also lacked love. Disappointments and tragedies were all too common. And now people saw her as a grumpy old woman. Thankfully, she still had the hope of heaven. The future has got to be better than this. And I would say she was right.<br/>
