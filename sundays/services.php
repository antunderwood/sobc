                    Dot Cotton (for anyone who doesn't know - an elderly lady who believes in God, quotes the bible, worries a lot, smokes almost as much, and who comes across as rather a sad character)... Dot Cotton has been chatting away to her tape recorder - giving a message to her husband, Jim, who is in hospital having had a stroke (so my wife tells me).<br/>
